          |[<(<(<(<{}))><([]([]()
          |<{([([[(<>()){}]>(<<{{
          |<{([{{}}[<[[[<>{}]]]>[]]
        """.stripMargin.trim

      val corruptedScore = SyntaxChecker.lintAll(input)

      val completedScore = SyntaxChecker.completeAll(input)

      assert(corruptedScore == Right(26397))
      assert(completedScore == Right(288957))
